package com.model;

public class ApplyProject {

	private int id;
	private String projectt;
	private String cname;
	private String cemail;
	private String fname;
	private String femail;
	private String resumef;
	private String status;
	
	public String getStatus() {
		return status;
	}
	public void setStatus(String status) {
		this.status = status;
	}
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getProjectt() {
		return projectt;
	}
	public void setProjectt(String projectt) {
		this.projectt = projectt;
	}
	public String getCname() {
		return cname;
	}
	public void setCname(String cname) {
		this.cname = cname;
	}
	public String getCemail() {
		return cemail;
	}
	public void setCemail(String cemail) {
		this.cemail = cemail;
	}
	public String getFname() {
		return fname;
	}
	public void setFname(String fname) {
		this.fname = fname;
	}
	public String getFemail() {
		return femail;
	}
	public void setFemail(String femail) {
		this.femail = femail;
	}
	public String getResumef() {
		return resumef;
	}
	public void setResumef(String resumef) {
		this.resumef = resumef;
	}
	
}
